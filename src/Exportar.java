void impresion() {

        String rutaCar = "C:\\Josefa Ortiz de Dominguez\\Evaluaciones";
        File carpeta = new File(rutaCar);
        String nombre = T_NombreArchivo.getText();
        String rutaArch = "";

        if (T_NombreArchivo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduzca un nombre para el archivo");
        }else{
            if (carpeta.exists()) {
                rutaArch = "C:\\Josefa Ortiz de Dominguez\\Evaluaciones\\" + nombre + ".xls";
                File file = new File(rutaArch);
                ExportarExcel excel = new ExportarExcel(TB_Evaluaciones, file, T_NombreArchivo.getText());
                excel.export();
                if(true){
                    JOptionPane.showMessageDialog(this, "Documento generado con exito \n" + "Espere...");
                }

            } else {
                carpeta.mkdirs();

                rutaArch = "C:\\Josefa Ortiz de Dominguez\\Evaluaciones\\" + nombre + ".xls";
                File file = new File(rutaArch);
                ExportarExcel excel = new ExportarExcel(TB_Evaluaciones, file, nombre);
                excel.export();
                if(true){
                    JOptionPane.showMessageDialog(this, "Documento generado con exito \n" + "Espere...");
                }
            }
        }
        
        try{
            ProcessBuilder p = new ProcessBuilder();
            p.command("cmd.exe", "/c", rutaArch);
            p.start();
        } catch (IOException ex) {
            Logger.getLogger(VerEvaluaciones.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Se intent칩 abrir el documento pero ocurri칩 un error");
        }
}
public void CargarArticulo() {
        DefaultTableModel modelo = (DefaultTableModel) TB_Evaluaciones.getModel();
        modelo.setRowCount(1);
        res = conexion.Consulta("SELECT Alumnos.NombreAlumno, Parciales.ComprensionMundo, Parciales.LenguajeComunicacion, Parciales.PensamientoMatematico, Parciales.Artes, Parciales.EducacionFisica FROM Alumnos INNER JOIN Parciales ON Alumnos.CURPAlumno=Parciales.CURPAlumno");
        //contador numero progresivo
        int c = 1;
        try {
            while (res.next()) {
                Vector v = new Vector();
                v.add(String.valueOf(c));
                v.add(res.getString(1));
                v.add(res.getString(2));
                v.add(res.getString(3));
                v.add(res.getString(4));
                v.add(res.getString(5));
                v.add(res.getString(6));

                modelo.addRow(v);
                TB_Evaluaciones.setModel(modelo);
                c++;
            }
        } catch (SQLException e) {

        }
    }
